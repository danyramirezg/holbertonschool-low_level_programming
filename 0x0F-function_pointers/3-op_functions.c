#include <stdlib.h>
#include "3-calc.h"

/**
 * op_add - Function that sum a and b
 * @a: The first function's parameter
 * @b: The second function's parameter
 *
 * Return: The sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function that makes the difference between a and b
 * @a: The first function's parameter
 * @b: The second function's parameter
 *
 * Return: The difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Product of a and b
 * @a: The first function's parameter
 * @b: The second function's parameter
 *
 * Return: The product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Division of a by b
 * @a: The first function's parameter
 * @b: The second function's parameter
 *
 * Return: The result of the division of a by b.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Remainder of the division of a by b
 * @a: The first function's parameter
 * @b: The second function's parameter
 *
 * Return: The sum of a and b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}

